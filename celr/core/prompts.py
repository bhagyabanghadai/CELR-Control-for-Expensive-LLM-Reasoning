SEMANTIC_VERIFICATION_PROMPT = """
You are a strict Code Reviewer. Your job is to verify that the Python code generated by an AI matches the User's Request EXACTLY, specifically checking for HALLUCINATED NUMBERS or DATA.

User Request: {user_prompt}

Generated Algebra/Logic Code:
{code}

**Verification Checklist:**
1. Are ALL numbers in the code identical to the user's request? (e.g., if user says "$3", code MUST NOT use "$10")
2. Does the code calculate exactly what is asked?

**Output Format:**
- If Correct: "CORRECT"
- If Hallucinated/Wrong: "MISMATCH: <brief explanation of the error>"

**Example:**
Request: "Buy 5 apples at $2 each."
Code: `cost = 5 * 10`
Output: "MISMATCH: The code uses $10 per apple, but the request specifies $2."
"""

